# plus
p = 3+2
print("2 + 3 = %d" % p)

# minus
m = 3-2
print("3 - 2 = %d" % m)

# multiply
mu = 3*2
print("3 * 2 = %d" % mu)

# divide
d = 3/2
print("3 / 2 = %.2f" % d)

# modulus
mo = 3%2
print("3 %% 2 = %d" % mo)

# // floor division
fd = 3//2
print("3 // 2 = %d" % fd)

# power
po = 3**2
print("3 ** 2 = %d" % po)

# logical operators
print("----Logical Operators----")
a = 10==10
b = 1>100

# and
print("True and False = %s" % str(a and b))

# or
print("True or False = %s" % str(a or b))

# not
print("not True = %s" % str(not a))